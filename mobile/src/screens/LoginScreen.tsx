import React, { useState } from 'react';
import {
  View,
  Text,
  TextInput,
  TouchableOpacity,
  StyleSheet,
  Alert,
  ActivityIndicator,
} from 'react-native';
import { useAuth } from '../context/AuthContext';

export default function LoginScreen({ navigation }: any) {
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [loading, setLoading] = useState(false);
  const { login } = useAuth();

  const handleLogin = async () => {
    if (!email || !password) {
      Alert.alert('Hata', 'Email ve ≈üifre gereklidir');
      return;
    }

    setLoading(true);
    try {
      await login(email, password);
      // Navigation handled by App.tsx based on auth state
    } catch (error: any) {
      if (error.message === 'MFA_REQUIRED') {
        navigation.navigate('MFA', { email, password });
      } else {
        Alert.alert('Giri≈ü Hatasƒ±', 'Email veya ≈üifre hatalƒ±');
      }
    } finally {
      setLoading(false);
    }
  };

  return (
    <View style={styles.container}>
      <View style={styles.header}>
        <Text style={styles.logo}>üè¶</Text>
        <Text style={styles.title}>Mini Bank</Text>
        <Text style={styles.subtitle}>Giri≈ü Yapƒ±n</Text>
      </View>

      <View style={styles.form}>
        <TextInput
          style={styles.input}
          placeholder="Email"
          value={email}
          onChangeText={setEmail}
          keyboardType="email-address"
          autoCapitalize="none"
          autoComplete="email"
        />

        <TextInput
          style={styles.input}
          placeholder="≈ûifre"
          value={password}
          onChangeText={setPassword}
          secureTextEntry
          autoCapitalize="none"
        />

        <TouchableOpacity
          style={[styles.button, loading && styles.buttonDisabled]}
          onPress={handleLogin}
          disabled={loading}
        >
          {loading ? (
            <ActivityIndicator color="#fff" />
          ) : (
            <Text style={styles.buttonText}>Giri≈ü Yap</Text>
          )}
        </TouchableOpacity>

        <TouchableOpacity
          style={styles.linkButton}
          onPress={() => navigation.navigate('Register')}
        >
          <Text style={styles.linkText}>Hesabƒ±nƒ±z yok mu? Kayƒ±t olun</Text>
        </TouchableOpacity>
      </View>
    </View>
  );
}

